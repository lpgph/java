/**
 * @author Peter Guo
 * @version 0.1
 * @ClassName BaseEnum
 * @Description 模块用枚举类接口
 * @date 17/04/25
 */
public interface BaseEnum {

    /**
     * 返回该对象的整型表示
     *
     * @return 整型
     */
    public int toInt();

    /**
     * 返回该对象的字符串表示
     *
     * @return 字符串
     */
    public String toString();

}
